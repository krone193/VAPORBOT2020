# --- Functions ------------------------------------------------------------------------------------------------------ #
# Obtain a random number, different from the given last -------------------------------------------------------------- #
def get_random_index_within_data(last: int, data: json) -> int:
    if len(data) > 1:
        new_rand = last
        while new_rand == last:
            new_rand = floor(random() * len(data))
    else:
        new_rand = 0
    return new_rand

